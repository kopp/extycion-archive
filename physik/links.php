<h4>Arbeitsger&auml;te</h4>
<p>Ich wurde schon verschiedentlich gefragt, mit was ich mir denn so beim Arbeiten am PC Arbeit erspare. Hier deshalb mal ein paar Programme, die ich immer wieder gerne verwende und f&uuml;r gut befunden habe:</p>
<ul>
<h5>Mathe</h5>
<li>Maxima: Kann analytisch Ausdr&uuml;cke berechnen, das Ergebnis in LaTeX-Code ausgeben, Gleichungen l&ouml;sen, Differentialgleichungen l&ouml;sen etc. Und das alles OpenSource. Die Graphische Oberfl&auml;che hei&szlig;t "wxMaxima"<br> homepage: <a href="http://wxmaxima.sourceforge.net" target=extern>link</a></li>
<li>Octave: Berechnet numerisch Gleichungen, Matrizen und was wei&szlig; ich alles. Kann mit einer einfachen Sprache programmiert werden. <br>Homepage: <a href="http://www.gnu.org/software/octave/" target=extern>link</a></li>
<li>Gnuplot: Kann Graphen von Funktionen toll darstellen und auch riesige Datenmengen in Sekundenschnelle in anschauliche Schaubilder umwandeln. <br> Homepage: <a href="http://www.gnuplot.info/" target=extern>link</a></li>


<h5>Schreiben</h5>
